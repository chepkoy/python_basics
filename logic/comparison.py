# Comparing values returns either true or false

# Equal to for this case it returns true
print(5==5)

# Equal to for this case it returns False
print(5 == 7)

# Not equal to returns true

print(5 != 7)

# Not equal to returns false

print(5 != 5)

# Greater than and less than

print(5 > 2)
print(2 < 3)

# Greater than or equal to and vice versa

print(5 <= 7)
print(10 >= 8)

# Checking if a variable is none

my_variable = None

print(my_variable is None)